        public static void Execute()
        {
            var input = FullInput;
            var movements = input.Split(Environment.NewLine)
                .Where(x => !string.IsNullOrEmpty(x))
                .Select(x => x.Split(" "))
                .Select(x => (Enum.Parse<Direction>(x[0], ignoreCase: true), int.Parse(x[1])))!;

            // Part 1 - Multiply final coordinates (depth x horizontal position)
            Console.WriteLine("Part 1 - " + Solve(movements));

            // Part 2 - Up/Down change aim, not depth; Forward changes depth by aim * amount;
            Console.WriteLine("Part 2 - " + Solve(movements, useAim: true));
        }

        private static int Solve(IEnumerable<(Direction Direction, int Amount)> movements, bool useAim = false)
        {
            var depth = 0;
            var aim = 0;
            var horizontalPosition = 0;

            foreach (var movement in movements)
            {
                switch (movement.Direction)
                {
                    case Direction.Forward:
                        horizontalPosition += movement.Amount;
                        if (useAim)
                            depth += aim * movement.Amount;
                        break;
                    case Direction.Up:
                        if (useAim)
                            aim -= movement.Amount;
                        else
                            depth -= movement.Amount;
                        break;
                    case Direction.Down:
                        if (useAim)
                            aim += movement.Amount;
                        else
                            depth += movement.Amount;
                        break;
                }
            }

            return depth * horizontalPosition;
        }

